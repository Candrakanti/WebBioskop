<h1 class="code-line" data-line-start=0 data-line-end=1><a id="User_Guide_0"></a>User Guide</h1>
<h2 class="code-line" data-line-start=1 data-line-end=2><a id="_User_Guide_Aplikasi_Pemesanan_Tiket_Bioskop__1"></a><em>User Guide Aplikasi Pemesanan Tiket Bioskop</em></h2>
<p class="has-line-data" data-line-start="7" data-line-end="8">Tentang Aplikasi</p>
<p class="has-line-data" data-line-start="9" data-line-end="10">Seenema merupakan aplikasi tiket bioskop yang berbasis website yang dapat di akses oleh masyarakat tanpa terkecuali. Aplikasi SeeNema dibuat untuk memudahkan masyarakat dalam memesan tiket bioskop secara online tanpa harus mengantri.</p>
<p class="has-line-data" data-line-start="11" data-line-end="12">User Guide Aplikasi Bioskop ini dibuat dengan tujuan sebagai berikut :</p>
<ul>
<li class="has-line-data" data-line-start="13" data-line-end="14">Menggambarkan dan menjelaskan pengunaan Aplikasi Bioskop untuk User.</li>
<li class="has-line-data" data-line-start="14" data-line-end="16">Sebagai panduan penggunaan Aplikasi Bioskop ini.</li>
</ul>
<p class="has-line-data" data-line-start="16" data-line-end="17">Deskripsi Umum Aplikasi :</p>
<ul>
<li class="has-line-data" data-line-start="17" data-line-end="19">Pembuatan aplikasi bioskop ini berbasis website, sistem yang dihasilkan diharapkan dapat mempermudah masyarakat untuk memesan tiket bioskop sesuai hari tanggal dan jam yang diinginkan tanpa mengantri.</li>
</ul>
<h2 class="code-line" data-line-start=19 data-line-end=20><a id="Fitur__Fitur_yang_terdapat_di_Aplikasi_Bioskop_19"></a>Fitur - Fitur yang terdapat di Aplikasi Bioskop</h2>
<ul>
<li class="has-line-data" data-line-start="21" data-line-end="23">
<h4 class="code-line" data-line-start=21 data-line-end=22><a id="Login_21"></a>Login</h4>
<ul>
<li class="has-line-data" data-line-start="22" data-line-end="23">Di Halaman Login ini user bisa memasukkan akun yang sudah kita buat.</li>
</ul>
</li>
<li class="has-line-data" data-line-start="23" data-line-end="27">
<h4 class="code-line" data-line-start=23 data-line-end=24><a id="Register_23"></a>Register</h4>
<ul>
<li class="has-line-data" data-line-start="24" data-line-end="25">Jika user belum mempunyai akun, kita harus register dengan mengisi nama, alamat email, nomor telepon, dan password.</li>
<li class="has-line-data" data-line-start="25" data-line-end="27">Jika user sudah membuat atau memasukkan akunnya, user bisa langsung melihat halaman tampilan Seenema.</li>
</ul>
</li>
<li class="has-line-data" data-line-start="27" data-line-end="31">
<h4 class="code-line" data-line-start=27 data-line-end=28><a id="Home_27"></a>Home</h4>
<ul>
<li class="has-line-data" data-line-start="28" data-line-end="31">Di Halaman home ini User dapat melihat rekomendasi film yang banyak ditonton<br>
atau film yang terbaik dan best seller.</li>
</ul>
</li>
<li class="has-line-data" data-line-start="31" data-line-end="39">
<h4 class="code-line" data-line-start=31 data-line-end=32><a id="Film_31"></a>Film</h4>
<ul>
<li class="has-line-data" data-line-start="32" data-line-end="33">User dapat melihat film yang sedang playing atau upcoming, juga dapat membooking film dan memilih jam tayang, dan tempat duduk.</li>
<li class="has-line-data" data-line-start="33" data-line-end="34">Playing : film yang sedang ditayangkan di bioskop.</li>
<li class="has-line-data" data-line-start="34" data-line-end="36">Upcoming : film yang akan segera tayang di aplikasi bioskop tersebut.<br>
Book Now : Booking atau sering kita sebut memesan yang artinya memesan tiket bioskop secara online tanpa harus datang untuk mengantri di tempat.</li>
<li class="has-line-data" data-line-start="36" data-line-end="38">Detail Film : Ketika kita sudah mengklik Book Now maka tampilan akan otomatis menampilkan halaman detail film yang berisi seperti produser, sutradara, penulis.<br>
Selain ada informasi film, di Detail Film ini terdapat Trailer Film untuk kita tonton seperti apa cupilkan film tersebut, serta terdapat sinopsis untuk user tau bagaimana jalannya cerita tersebut.</li>
<li class="has-line-data" data-line-start="38" data-line-end="39">User bisa melihat detail film tanpa harus login, sedangkan jika user ingin memilih dan memesan tiket maka user diharuskan untuk login (Masukkann Akunnya) dan bila user tidak memiliki akun, user harus register terlebih dahulu.</li>
</ul>
</li>
<li class="has-line-data" data-line-start="39" data-line-end="42">
<h4 class="code-line" data-line-start=39 data-line-end=40><a id="Jam_Tayang_39"></a>Jam Tayang</h4>
<ul>
<li class="has-line-data" data-line-start="40" data-line-end="41">Setelah melihat detail film tersebut user bisa menscroll ke bawah, akan ada jadwal tayang film di hari itu, serta bulan, dan tanggalnya.</li>
<li class="has-line-data" data-line-start="41" data-line-end="42">Apabila user sudah melihat jadwal tayang filmnya, user bisa langsung mengklik mall yang tersedia dan mengklik jam tayang yang ada.</li>
</ul>
</li>
<li class="has-line-data" data-line-start="42" data-line-end="46">
<h4 class="code-line" data-line-start=42 data-line-end=43><a id="Seat_42"></a>Seat</h4>
<ul>
<li class="has-line-data" data-line-start="43" data-line-end="44">Di Tampilan Seat ini user bisa memilih kursi sesuai keinginannya, selain bisa memilih kursi di tampilan ini user bisa memilih regular atau velvet dan user bisa melihat detail pemesanannya seperti ada mall, jam tayang, dan studionya.</li>
<li class="has-line-data" data-line-start="44" data-line-end="45">Setelah user sudah fiks dengan pesanannya, user bisa mengklik Book Now yang akan menampilkan harga keseluruhannya, selain ada harga akan ada nama dan email user yang memesan film tersebut. Lalu user bisa memilih metode pembayaran yang ada yaitu bank mandiri.</li>
<li class="has-line-data" data-line-start="45" data-line-end="46">User yang sudah memilih metode pembayaran akan muncul detail pembayaran yang berisi Virtual Account Number dan jumlah yang harus dibayar.</li>
</ul>
</li>
<li class="has-line-data" data-line-start="46" data-line-end="53">
<h4 class="code-line" data-line-start=46 data-line-end=47><a id="MySeenema_46"></a>MySeenema</h4>
<ul>
<li class="has-line-data" data-line-start="48" data-line-end="49">Halaman MySeenema ini ada menu seperti Belum Bayar, Selesai, Pembatalan Pesanan dan Bank Saya.</li>
<li class="has-line-data" data-line-start="49" data-line-end="50">Menu Belum Bayar ini berisi pesanan yang user belum di selesaikan pembayarannya.</li>
<li class="has-line-data" data-line-start="50" data-line-end="51">Menu Selesai ini berisi pesanan yang pembayarannya sudah diselesaikan oleh user.</li>
<li class="has-line-data" data-line-start="51" data-line-end="52">Menu Pembatalan Pesanan ini berisi pesanan yang pembayarannya belum di selesaikan oleh user, karna durasi pembayaran yang sudah habis.</li>
<li class="has-line-data" data-line-start="52" data-line-end="53">Bank Saya ini berisi payment code yang sudah tercantum di pesanan yang dipesan oleh user, dan user harus menyalin codenya untuk mengisi payment code lalu menyelesaikan pembayaran pesanannya.</li>
</ul>
</li>
</ul>
